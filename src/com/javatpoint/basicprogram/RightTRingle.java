package com.javatpoint.basicprogram;

public class RightTRingle  

{   

public static void main(String args[])   

{   

// the rows to print  

int i, j, row=6;   

//the outer loop for rows  

for(i=0; i<row; i++)   

{   

//the inner loop for columns  

for(j=0; j<=i; j++)   

{   

//to prints stars   

System.out.println("*");   

}   

// print new line 

System.out.println();   

}   

}   

} 